def _hdr(cell, kind="fixed"):
    cell.font   = FONT_LOCK if kind == "lock" else (FONT_TAG if kind == "tag" else FONT_FIXED)
    cell.fill   = FILL_LOCK if kind == "lock" else (FILL_TAG if kind == "tag" else FILL_FIXED)
    cell.alignment = H_ALIGN
    cell.border = BORDER


def _dat(cell, alt=False):
    cell.alignment = D_ALIGN
    cell.border    = BORDER
    if alt:
        cell.fill = FILL_ALT


