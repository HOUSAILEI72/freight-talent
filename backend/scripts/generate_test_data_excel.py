def make_jobs_xlsx():
    wb = Workbook()
    ws = wb.active
    ws.title = "岗位"

    # 表头
    for col, h in enumerate(JOB_ALL_COLS, start=1):
        cell = ws.cell(row=1, column=col, value=h)
        _hdr(cell, kind="fixed" if h in JOB_FIXED else "tag")

    # 数据行
    for r, row in enumerate(JOBS, start=2):
        alt = (r % 2 == 0)
        for c, val in enumerate(row, start=1):
            cell = ws.cell(row=r, column=c, value=val)
            _dat(cell, alt)

    # 列宽
    widths = [22, 28, 10, 12, 10, 10, 8, 52, 36, 28, 28,
              10, 10, 10, 14, 14, 12, 10, 20, 12]
    for i, w in enumerate(widths[:len(JOB_ALL_COLS)], start=1):
        ws.column_dimensions[get_column_letter(i)].width = w
    ws.row_dimensions[1].height = 30
    for r in range(2, len(JOBS) + 2):
        ws.row_dimensions[r].height = 90
    ws.freeze_panes = "A2"

    # 说明 sheet
    _add_job_notes(wb)

    out = OUT_DIR / "jobs_test_data.xlsx"
    wb.save(out)
    return out


